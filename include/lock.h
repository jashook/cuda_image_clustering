/* ************************************************************************** */

#ifdef _WIN32

#include <windows.h>

#else

#include <pthread.h>

#endif

#include <string.h>

/* ************************************************************************** */
/* ************************************************************************** */

typedef struct lock
{
    #ifdef _WIN32
        CRITICAL_SECTION mutex;
    #else
        pthread_mutex_t mutex;
    #endif

} lock;

/* ************************************************************************** */
/* ************************************************************************** */

void lock_delete(lock*);
void lock_get(lock*);
void lock_init(lock*);
void lock_release_t(lock*);

